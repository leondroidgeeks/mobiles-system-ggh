    <li>das Bestehen eines Rechts auf Berichtigung oder L&ouml;schung der Sie betreffenden personenbezogenen Daten, eines Rechts auf Einschr&auml;nkung der Verarbeitung durch den Verantwortlichen oder eines Widerspruchsrechts gegen diese Verarbeitung;</li>
    <li>das Bestehen eines Beschwerderechts bei einer Aufsichtsbeh&ouml;rde;</li>
    <li>alle verf&uuml;gbaren Informationen &uuml;ber die Herkunft der Daten, wenn die personenbezogenen Daten nicht bei der betroffenen Person erhoben werden;</li>
    <li>das Bestehen einer automatisierten Entscheidungsfindung einschlie&szlig;lich Profiling gem&auml;&szlig; Art. 22 Abs. 1 und 4 DSGVO und - zumindest in diesen F&auml;llen - aussagekr&auml;ftige Informationen &uuml;ber die involvierte Logik sowie die
      Tragweite und die angestrebten Auswirkungen einer derartigen Verarbeitung f&uuml;r die betroffene Person.</li>
  </ul>
  <p>
    Ihnen steht das Recht zu, Auskunft dar&uuml;ber zu verlangen, ob die Sie betreffenden personenbezogenen Daten in ein Drittland oder an eine internationale Organisation &uuml;bermittelt werden. In diesem Zusammenhang k&ouml;nnen Sie verlangen, &uuml;ber
    die geeigneten Garantien gem. Art. 46 DSGVO im Zusammenhang mit der &Uuml;bermittlung unterrichtet zu werden.
  </p>
  <p>
    Bei Datenverarbeitung zu wissenschaftlichen, historischen oder statistischen Forschungszwecken kann dieses Auskunftsrecht insoweit beschr&auml;nkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke unm&ouml;glich macht
    oder ernsthaft beeintr&auml;chtigt und die Beschr&auml;nkung f&uuml;r die Erf&uuml;llung der Forschungs- oder Statistikzwecke notwendig ist
  </p>

  <h4>2. Recht auf Berichtigung</h4>
  <p>
    Sie haben ein Recht auf Berichtigung und/oder Vervollst&auml;ndigung gegen&uuml;ber dem Verantwort-lichen, sofern die verarbeiteten personenbezogenen Daten, die Sie betreffen, unrichtig oder unvollst&auml;ndig sind. Der Verantwortliche hat die Berichtigung
    unverz&uuml;glich vorzunehmen.
  </p>
  <p>
    Bei Datenverarbeitung zu wissenschaftlichen, historischen oder statistischen Forschungszwecken kann Ihr Recht auf Berichtigung insoweit beschr&auml;nkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke unm&ouml;glich
    macht oder ernsthaft beein-tr&auml;chtigt und die Beschr&auml;nkung f&uuml;r die Erf&uuml;llung der Forschungs- oder Statistikzwecke not-wendig ist.
  </p>

  <h4>3.	Recht auf Einschr&auml;nkung der Verarbeitung</h4>
  <p>
    Unter den folgenden Voraussetzungen k&ouml;nnen Sie die Einschr&auml;nkung der Verarbeitung der Sie betreffenden personenbezogenen Daten verlangen:
  </p>
  <ul class="numbered">
    <li>wenn Sie die Richtigkeit der Sie betreffenden personenbezogenen f&uuml;r eine Dauer be-streiten, die es dem Verantwortlichen erm&ouml;glicht, die Richtigkeit der personenbezogenen Daten zu &uuml;berpr&uuml;fen;</li>
    <li>die Verarbeitung unrechtm&auml;&szlig;ig ist und Sie die L&ouml;schung der personenbezogenen Daten ablehnen und stattdessen die Einschr&auml;nkung der Nutzung der personenbezogenen Daten verlangen;</li>
    <li>der Verantwortliche die personenbezogenen Daten f&uuml;r die Zwecke der Verarbeitung nicht l&auml;nger ben&ouml;tigt, Sie diese jedoch zur Geltendmachung, Aus&uuml;bung oder Verteidigung von Rechtsanspr&uuml;chen ben&ouml;tigen, oder</li>
    <li>wenn Sie Widerspruch gegen die Verarbeitung gem&auml;&szlig; Art. 21 Abs. 1 DSGVO eingelegt haben und noch nicht feststeht, ob die berechtigten Gr&uuml;nde des Verantwortlichen gegen&uuml;ber Ihren Gr&uuml;nden &uuml;berwiegen.</li>
  </ul>
  <p>
    Wurde die Verarbeitung der Sie betreffenden personenbezogenen Daten eingeschr&auml;nkt, d&uuml;rfen diese Daten - von ihrer Speicherung abgesehen - nur mit Ihrer Einwilligung oder zur Geltendmachung, Aus&uuml;bung oder Verteidigung von Rechtsanspr&uuml;chen
    oder zum Schutz der Rechte einer anderen nat&uuml;rlichen oder juristischen Person oder aus Gr&uuml;nden eines wichtigen &ouml;ffentlichen Interesses der Union oder eines Mitgliedstaats verarbeitet werden.</p>
  <p>
    Wurde die Einschr&auml;nkung der Verarbeitung nach den o.g. Voraussetzungen eingeschr&auml;nkt, werden Sie von dem Verantwortlichen unterrichtet, bevor die Einschr&auml;nkung aufgehoben wird.
  </p>
  <p>
    Ihr Recht auf Einschr&auml;nkung der Verarbeitung kann insoweit beschr&auml;nkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke unm&ouml;glich macht oder ernsthaft beeintr&auml;chtigt und die Beschr&auml;nkung f&uuml;r
    die Erf&uuml;llung der Forschungs- oder Statistikzwecke notwendig ist.
  </p>

  <h4>4. Recht auf L&ouml;schung</h4>
  <h5>a) L&ouml;schungspflicht</h5>
  <p>
    Sie k&ouml;nnen von dem Verantwortlichen verlangen, dass die Sie betreffenden personenbezogenen Daten unverz&uuml;glich gel&ouml;scht werden, und der Verantwortliche ist verpflichtet, diese Daten unverz&uuml;glich zu l&ouml;schen, sofern einer der folgenden
    Gr&uuml;nde zutrifft:
  </p>
  <ul class="numbered">
    <li>Die Sie betreffenden personenbezogenen Daten sind f&uuml;r die Zwecke, f&uuml;r die sie erhoben oder auf sonstige Weise verarbeitet wurden, nicht mehr notwendig.</li>
    <li>Sie widerrufen Ihre Einwilligung, auf die sich die Verarbeitung gem. Art. 6 Abs. 1 lit. a oder Art. 9 Abs. 2 lit. a DSGVO st&uuml;tzte, und es fehlt an einer anderweitigen Rechtsgrundlage f&uuml;r die Verarbeitung.</li>
    <li>Sie legen gem. Art. 21 Abs. 1 DSGVO Widerspruch gegen die Verarbeitung ein und es liegen keine vorrangigen berechtigten Gr&uuml;nde f&uuml;r die Verarbeitung vor, oder Sie legen gem. Art. 21 Abs. 2 DSGVO Widerspruch gegen die Verarbeitung ein.
    </li>
    <li>Die Sie betreffenden personenbezogenen Daten wurden unrechtm&auml;&szlig;ig verarbeitet.</li>
    <li>Die L&ouml;schung der Sie betreffenden personenbezogenen Daten ist zur Erf&uuml;llung einer rechtlichen Verpflichtung nach dem Unionsrecht oder dem Recht der Mitgliedstaaten erforderlich, dem der Verantwortliche unterliegt.</li>
    <li>Die Sie betreffenden personenbezogenen Daten wurden in Bezug auf angebotene Dien-ste der Informationsgesellschaft gem&auml;&szlig; Art. 8 Abs. 1 DSGVO erhoben.</li>
  </ul>
  <h5>b) Information an Dritte</h5>
  <p>
    Hat der Verantwortliche die Sie betreffenden personenbezogenen Daten &ouml;ffentlich gemacht und ist er gem. Art. 17 Abs. 1 DSGVO zu deren L&ouml;schung verpflichtet, so trifft er unter Ber&uuml;cksichtigung der verf&uuml;gbaren Technologie und der Implementierungskosten
    angemessene Ma&szlig;nahmen, auch technischer Art, um f&uuml;r die Datenverarbeitung Verantwortliche, die die personenbezogenen Daten verarbeiten, dar&uuml;ber zu informieren, dass Sie als betroffene Person von ihnen die L&ouml;schung aller Links
    zu diesen personenbezogenen Daten oder von Kopien oder Replikationen dieser personenbezogenen Daten verlangt haben.
  </p>

  <h5>c) Ausnahmen</h5> Das Recht auf L&ouml;schung besteht nicht, soweit die Verarbeitung erforderlich ist
  <ul class="numbered">
    <li>zur Aus&uuml;bung des Rechts auf freie Meinungs&auml;u&szlig;erung und Information;</li>
    <li>zur Erf&uuml;llung einer rechtlichen Verpflichtung, die die Verarbeitung nach dem Recht der Union oder der Mitgliedstaaten, dem der Verantwortliche unterliegt, erfordert, oder zur Wahrnehmung einer Aufgabe, die im &ouml;ffentlichen Interesse liegt
      oder in Aus&uuml;bung &ouml;ffentlicher Gewalt erfolgt, die dem Verantwortlichen &uuml;bertragen wurde;</li>
    <li>aus Gr&uuml;nden des &ouml;ffentlichen Interesses im Bereich der &ouml;ffentlichen Gesundheit gem&auml;&szlig; Art. 9 Abs. 2 lit. h und i sowie Art. 9 Abs. 3 DSGVO;</li>
    <li>f&uuml;r im &ouml;ffentlichen Interesse liegende Archivzwecke, wissenschaftliche oder historische Forschungszwecke oder f&uuml;r statistische Zwecke gem. Art. 89 Abs. 1 DSGVO, soweit das unter Abschnitt a) genannte Recht voraussichtlich die Verwirklichung
      der Ziele dieser Verarbeitung unm&ouml;glich macht oder ernsthaft beeintr&auml;chtigt, oder</li>
    <li>zur Geltendmachung, Aus&uuml;bung oder Verteidigung von Rechtsanspr&uuml;chen.</li>
  </ul>

  <h4>5. Recht auf Unterrichtung</h4>
  <p>
    Haben Sie das Recht auf Berichtigung, L&ouml;schung oder Einschr&auml;nkung der Verarbeitung ge-gen&uuml;ber dem Verantwortlichen geltend gemacht, ist dieser verpflichtet, allen Empf&auml;ngern, denen die Sie betreffenden personenbezogenen Daten offengelegt
    wurden, diese Berichtigung oder L&ouml;schung der Daten oder Einschr&auml;nkung der Verarbeitung mitzuteilen, es sei denn, dies erweist sich als unm&ouml;glich oder ist mit einem unverh&auml;ltnism&auml;&szlig;igen Aufwand verbunden.
  </p>
  <p>
    Ihnen steht gegen&uuml;ber dem Verantwortlichen das Recht zu, &uuml;ber diese Empf&auml;nger unterrichtet zu werden.
  </p>

  <h4>6. Recht auf Daten&uuml;bertragbarkeit</h4>
  <p>
    Sie haben das Recht, die Sie betreffenden personenbezogenen Daten, die Sie dem Verantwort-lichen bereitgestellt haben, in einem strukturierten, g&auml;ngigen und maschinenlesbaren Format zu erhalten. Au&szlig;erdem haben Sie das Recht diese Daten einem
    anderen Verantwortlichen ohne Behinderung durch den Verantwortlichen, dem die personenbezogenen Daten bereitgestellt wurden, zu &uuml;bermitteln, sofern
  </p>
  <ul class="numbered">
    <li>die Verarbeitung auf einer Einwilligung gem. Art. 6 Abs. 1 lit. a DSGVO oder Art. 9 Abs. 2 lit. a DSGVO oder auf einem Vertrag gem. Art. 6 Abs. 1 lit. b DSGVO beruht und</li>
    <li>die Verarbeitung mithilfe automatisierter Verfahren erfolgt.</li>
  </ul>
  <p>
    In Aus&uuml;bung dieses Rechts haben Sie ferner das Recht, zu erwirken, dass die Sie betreffenden personenbezogenen Daten direkt von einem Verantwortlichen einem anderen Verantwortlichen &uuml;bermittelt werden, soweit dies technisch machbar ist. Freiheiten
    und Rechte anderer Personen d&uuml;rfen hierdurch nicht beeintr&auml;chtigt werden.
  </p>
  <p>
    Das Recht auf Daten&uuml;bertragbarkeit gilt nicht f&uuml;r eine Verarbeitung personenbezogener Daten, die f&uuml;r die Wahrnehmung einer Aufgabe erforderlich ist, die im &ouml;ffentlichen Interesse liegt oder in Aus&uuml;bung &ouml;ffentlicher Gewalt
    erfolgt, die dem Verantwortlichen &uuml;bertragen wurde.
  </p>

  <h4>7. Widerspruchsrecht</h4>
  <p>
    Sie haben das Recht, aus Gr&uuml;nden, die sich aus ihrer besonderen Situation ergeben, jederzeit gegen die Verarbeitung der Sie betreffenden personenbezogenen Daten, die aufgrund von Art. 6 Abs. 1 lit. e oder f DSGVO erfolgt, Widerspruch einzulegen;
    dies gilt auch f&uuml;r ein auf diese Bestimmungen gest&uuml;tztes Profiling.</p>
  <p>
    Der Verantwortliche verarbeitet die Sie betreffenden personenbezogenen Daten nicht mehr, es sei denn, er kann zwingende schutzw&uuml;rdige Gr&uuml;nde f&uuml;r die Verarbeitung nachweisen, die Ihre Interessen, Rechte und Freiheiten &uuml;berwiegen, oder
    die Verarbeitung dient der Geltendmachung, Aus&uuml;bung oder Verteidigung von Rechtsanspr&uuml;chen.
  </p>
  <p>
    Werden die Sie betreffenden personenbezogenen Daten verarbeitet, um Direktwerbung zu betreiben, haben Sie das Recht, jederzeit Widerspruch gegen die Verarbeitung der Sie betreffenden personenbezogenen Daten zum Zwecke derartiger Werbung einzulegen; dies
    gilt auch f&uuml;r das Profiling, soweit es mit solcher Direktwerbung in Verbindung steht.
  </p>
  <p>
    Widersprechen Sie der Verarbeitung f&uuml;r Zwecke der Direktwerbung, so werden die Sie betreffenden personenbezogenen Daten nicht mehr f&uuml;r diese Zwecke verarbeitet.
  </p>
  <p>
    Sie haben die M&ouml;glichkeit, im Zusammenhang mit der Nutzung von Diensten der Informa-tionsgesellschaft – ungeachtet der Richtlinie 2002/58/EG – Ihr Widerspruchsrecht mittels automatisierter Verfahren auszu&uuml;ben, bei denen technische Spezifikationen
    verwendet werden.
  </p>
  <p>
    Sie haben auch das Recht, aus Gr&uuml;nden, die sich aus Ihrer besonderen Situation ergeben, bei der Verarbeitung Sie betreffender personenbezogener Daten, die zu wissenschaftlichen oder historischen Forschungszwecken oder zu statistischen Zwecken gem.
    Art. 89 Abs. 1 DSGVO erfolgt, dieser zu widersprechen.
  </p>
  <p>
    Ihr Widerspruchsrecht kann insoweit beschr&auml;nkt werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke unm&ouml;glich macht oder ernsthaft beeintr&auml;chtigt und die Beschr&auml;nkung f&uuml;r die Erf&uuml;llung der
    Forschungs- oder Statistikzwecke notwendig ist.
  </p>
  <h4>8. Recht auf Widerruf der datenschutzrechtlichen Einwilligungserkl&auml;rung</h4>
  <p>
    Sie haben das Recht, Ihre datenschutzrechtliche Einwilligungserkl&auml;rung jederzeit zu widerrufen. Durch den Widerruf der Einwilligung wird die Rechtm&auml;&szlig;igkeit der aufgrund der Einwilligung bis zum Widerruf erfolgten Verarbeitung nicht ber&uuml;hrt.
  </p>
  <h4>9. Automatisierte Entscheidung im Einzelfall einschlie&szlig;lich Profiling</h4>
  <p>
    Sie haben das Recht, nicht einer ausschlie&szlig;lich auf einer automatisierten Verarbeitung – ein-schlie&szlig;lich Profiling – beruhenden Entscheidung unterworfen zu werden, die Ihnen gegen&uuml;ber rechtliche Wirkung entfaltet oder Sie in &auml;hnlicher
    Weise erheblich beeintr&auml;chtigt. Dies gilt nicht, wenn die Entscheidung
  </p>
  <ul class="numbered">
    <li>f&uuml;r den Abschluss oder die Erf&uuml;llung eines Vertrags zwischen Ihnen und dem Verant-wortlichen erforderlich ist,</li>
    <li>aufgrund von Rechtsvorschriften der Union oder der Mitgliedstaaten, denen der Ver-antwortliche unterliegt, zul&auml;ssig ist und diese Rechtsvorschriften angemessene Ma&szlig;nahmen zur Wahrung Ihrer Rechte und Freiheiten sowie Ihrer berechtigten
      Interessen enthalten oder</li>
    <li>mit Ihrer ausdr&uuml;cklichen Einwilligung erfolgt.</li>
  </ul>
  <p>
    Allerdings d&uuml;rfen diese Entscheidungen nicht auf besonderen Kategorien personenbezogener Daten nach Art. 9 Abs. 1 DSGVO beruhen, sofern nicht Art. 9 Abs. 2 lit. a oder g DSGVO gilt und angemessene Ma&szlig;nahmen zum Schutz der Rechte und Freiheiten
    sowie Ihrer berechtigten Interessen getroffen wurden.
  </p>
  <p>
    Hinsichtlich der in (1) und (3) genannten F&auml;lle trifft der Verantwortliche angemessene Ma&szlig;nahmen, um die Rechte und Freiheiten sowie Ihre berechtigten Interessen zu wahren, wozu mindestens das Recht auf Erwirkung des Eingreifens einer Person
    seitens des Verantwortlichen, auf Darlegung des eigenen Standpunkts und auf Anfechtung der Entscheidung geh&ouml;rt.
  </p>
  <h4>10. Recht auf Beschwerde bei einer Aufsichtsbeh&ouml;rde</h4>
  <p>
    Unbeschadet eines anderweitigen verwaltungsrechtlichen oder gerichtlichen Rechtsbehelfs steht Ihnen das Recht auf Beschwerde bei einer Aufsichtsbeh&ouml;rde, insbesondere in dem Mitgliedstaat ihres Aufenthaltsorts, ihres Arbeitsplatzes oder des Orts des
    mutma&szlig;lichen Versto&szlig;es, zu, wenn Sie der Ansicht sind, dass die Verarbeitung der Sie betreffenden perso-nenbezogenen Daten gegen die DSGVO verst&ouml;&szlig;t.
  </p>
  <p>
    Die Aufsichtsbeh&ouml;rde, bei der die Beschwerde eingereicht wurde, unterrichtet den Beschwerdef&uuml;hrer &uuml;ber den Stand und die Ergebnisse der Beschwerde einschlie&szlig;lich der M&ouml;glichkeit eines gerichtlichen Rechtsbehelfs nach Art. 78
    DSGVO.
  </p>
